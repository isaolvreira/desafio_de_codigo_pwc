def capitalizar_frase(frase):
    string_capitalizada = frase.title()
    return string_capitalizada

frase1 = capitalizar_frase('ana foi correr na praia')
print(frase1)

frase2 = capitalizar_frase('fui ao mercado na segunda-feira.')
print(frase2)

